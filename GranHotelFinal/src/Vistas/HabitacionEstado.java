        // TODO add your handling code here:
        TipoDeHabitacion tiha= (TipoDeHabitacion)JCBtipoDeHabitacion.getSelectedItem();
        
        ABMhabitacion h= new ABMhabitacion();
        
        borraFila();
        
        for(Habitacion hab: h.gesttionHabitacion(tiha.getIdtipoDeHabitacion())){
            
            modelo.addRow(new Object[]{hab.getTipo(), hab.isEstado()+" desocupada"});
            
            
            
        }
        
