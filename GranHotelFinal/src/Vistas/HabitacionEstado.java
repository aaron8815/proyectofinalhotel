    public void cabeceraTabla(){
        modelo.addColumn(" tipo de habitacion");
        modelo.addColumn("estado actual");
        tablaDehabitacionesPorTipo.setModel(modelo);
        
    }
    
    private void borraFila(){
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
    }


